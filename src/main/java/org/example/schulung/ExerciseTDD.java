package org.example.schulung;

public class ExerciseTDD {

    public static int sum(int a, int b) {
        return a + b;
    }

    public static boolean isEven(int number) {
        return number % 2 == 0;
    }

    public static String getFirstCharacter(String text) {
        return text.substring(0, 1);
    }
}
